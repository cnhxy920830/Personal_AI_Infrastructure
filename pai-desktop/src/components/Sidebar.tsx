import { useI18nStore } from "../store/useI18nStore";

interface SidebarProps {
  activePanel: string;
  setActivePanel: (panel: string) => void;
}

export function Sidebar({ activePanel, setActivePanel }: SidebarProps) {
  const { t } = useI18nStore();
  
  const items = [
    { id: "chat", icon: ChatIcon, label: t.sidebar.chat },
    { id: "skills", icon: SkillsIcon, label: t.sidebar.skills },
    { id: "memory", icon: MemoryIcon, label: t.sidebar.memory },
    { id: "settings", icon: SettingsIcon, label: t.sidebar.settings },
  ];

  return (
    <nav class="sidebar">
      {items.map((item) => (
        <button
          key={item.id}
          class={`sidebar-item ${activePanel === item.id ? "active" : ""}`}
          onClick={() => setActivePanel(item.id)}
          title={item.label}
        >
          <item.icon />
        </button>
      ))}
    </nav>
  );
}

